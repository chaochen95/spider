#urlencode()转url编码 参数需要一个dict类型
wd = urllib.urlencode(wd)

fullurl = url + "?" + wd

print(fullurl)

request = urllib2.Request(fullurl)
request.add_header("User-Agent", user_agent)
response = urllib2.urlopen(request)

print(response.read())
