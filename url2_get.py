#但 raw_input() 直接读取控制台的输入（任何类型的输入它都可以接收）。
#而对于 input() ，它希望能够读取一个合法的 python 表达式，
#即你输入字符串的时候必须使用引号将它括起来，否则它会引发一个 SyntaxError 。
#除非对 input() 有特别需要，否则一般情况下我们都是推荐使用 raw_input() 来与用户交互。
keyword = raw_input("输入关键词：")

